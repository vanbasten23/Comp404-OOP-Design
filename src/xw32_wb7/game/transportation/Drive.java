/**
 * Subclass of Transportation, a kind of transportation.
 * @author Wenbo Bu, Xiongfei Wei, Master of Computer Science
 * 		   Department of Computer Science, Rice University
 * @Version Created on Dec 4, 2014, 12:05:06 AM
 */
public class Drive extends Transportation {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8262055763881137120L;

	/**
	 * Construct this drive transportation object.
	 */
	public Drive(){
		this(4, 200);
		name = "Drive";
	}
	
	/**
	 * Construct this drive transportation object using time spent
	 * and price of the transportation.
	 * @param time The time spent.
	 * @param price The price.
	 */
	private Drive(int time, int price) {
		super(time, price);
	}

}
